const fruits = ['mango', 'papaya', 'pineapple', 'apple'];

// Iterate over fruits below
fruits.forEach(fruitIterator => {
  console.log('I want to eat a '+fruitIterator);
})
/* This lesson taught about the forEach iterator.
We can call forEach with a anonymous function to pass an iterator to a block of code. This is the same as using a for loop but frees us from keeping count of the index.
*/
